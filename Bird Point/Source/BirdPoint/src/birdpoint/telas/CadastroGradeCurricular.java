    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        if (Util.chkVazio(tfNomeGrade.getText(), tfNomeCurso.getText())) {
            gradeCurricular.setNomeGradeCurricular(tfNomeGrade.getText().toUpperCase());
            gradeCurricular.setCurso(curso);
            gradeCurricularDAO.salvar(gradeCurricular);
            btLimparActionPerformed(null);
        }
    }//GEN-LAST:event_btSalvarActionPerformed

    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        if (disciplina.getIdDisciplina() == 0) {
            if (gradeCurricular.getIdGradeCurricular() != 0) {
                if (JOptionPane.showConfirmDialog(rootPane, "Deseja excluir a Grade Curricular " + gradeCurricular.getNomeGradeCurricular()
                        + "?", "BirdPoint", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE) == JOptionPane.YES_OPTION) {
                    if (gradeCurricularDAO.remover(gradeCurricular)) {
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Não foi possível excluir a Grade Curricular " + gradeCurricular.getNomeGradeCurricular(),
                                "Erro ao Excluir", JOptionPane.ERROR_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(rootPane, "A exclusão foi cancelada!");
                }

            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Não foi possível excluir a Grade Curricular " + gradeCurricular.getNomeGradeCurricular() + " do Curso de " + gradeCurricular.getCurso().getNomeCurso()
                    + "\n pois já foram cadastradas disciplinas nela!", "Erro ao Excluir", JOptionPane.ERROR_MESSAGE);
        }
        btLimparActionPerformed(null);

    }//GEN-LAST:event_btExcluirActionPerformed

