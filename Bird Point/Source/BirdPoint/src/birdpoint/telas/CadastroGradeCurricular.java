    private void btPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPesquisarActionPerformed
        if (curso.getIdCurso() != 0) {
            List<GradeCurricular> lista;
            List<Disciplina> listaDisciplina;
            lista = (gradeCurricularDAO.listar());
            List<GradeCurricular> listaFiltradaGrade = new ArrayList<>();
            for (GradeCurricular gradeCurricular1 : lista) {
                if (gradeCurricular1.getCurso().getIdCurso() == curso.getIdCurso()) {
                    listaFiltradaGrade.add(gradeCurricular1);
                }
            }
            GradeCurricularTableModel itm = new GradeCurricularTableModel(listaFiltradaGrade);
            Object objetoRetorno = PesquisaGenerica.exibeTela(itm, "Grade Curricular");
            if (objetoRetorno != null) {
                gradeCurricular = gradeCurricularDAO.consultarObjetoId("idGradeCurricular", objetoRetorno);
                tfNomeGrade.setText(gradeCurricular.getNomeGradeCurricular());
                tfNomeCurso.setText(gradeCurricular.getCurso().getNomeCurso());
                curso = gradeCurricular.getCurso();

                listaDisciplina = (disciplinaDAO.listar());
                List<Disciplina> listaFiltrada = new ArrayList<>();

                for (Disciplina disciplina1 : listaDisciplina) {
                    if (disciplina1.getSemestre().getIdSemestre() == gradeCurricular.getIdGradeCurricular()) {
                        listaFiltrada.add(disciplina1);
                        disciplina = disciplina1;
                    }
                }

                btExcluir.setEnabled(true);
            }

        } else {
            JOptionPane.showMessageDialog(null, "Selecione o Curso!", "Erro", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btPesquisarActionPerformed

