    private void tfNomeGradeKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfNomeGradeKeyTyped
        int ascii = evt.getKeyChar();
        if (!(ascii >= 48 && ascii <= 57) && !(ascii == 46) && !(ascii == evt.VK_BACK_SPACE)) {
            evt.consume();
        }
        if (tfNomeGrade.getText().length() >= 6) {
            evt.consume();
        }
    }//GEN-LAST:event_tfNomeGradeKeyTyped

