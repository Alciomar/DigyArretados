    private void btPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPesquisarActionPerformed
        if (curso.getIdCurso() != 0 && !String.valueOf(jcAno.getSelectedItem()).equals("-----")) {
            List<Despesas> lista;
            lista = (despesasDAO.checkExistseq("anoDispesa", jcAno.getSelectedItem()));
            List<Despesas> listaFiltrada = new ArrayList<>();
            for (Despesas despesaTemp : lista) {
                if (despesaTemp.getCurso().getIdCurso() == curso.getIdCurso()) {
                    listaFiltrada.add(despesaTemp);
                }
            }
            DespesasTableModel itm = new DespesasTableModel(listaFiltrada);
            Object objetoRetorno = PesquisaGenerica.exibeTela(itm, "Despesa");
            if (objetoRetorno != null) {
                despesa = despesasDAO.consultarObjetoId("idDespesa", objetoRetorno);
                curso = despesa.getCurso();
                tfCurso.setText(despesa.getCurso().getNomeCurso());
                tfDespesasDeEncargos.setText(String.valueOf(despesa.getDespesasDeEncargos()));
                tfDespesasGerais.setText(String.valueOf(despesa.getDespesasGerais()));
                tfDespesasOperacionais.setText(String.valueOf(despesa.getDespesasOperacionais()));
                tfReceita.setText(String.valueOf(despesa.getReceitaCurso()));
                tfSalarioCoor.setText(String.valueOf(despesa.getSalarioCoor()));
                tfSalarioProf.setText(String.valueOf(despesa.getSalarioProf()));
                tfSalarioTecnic.setText(String.valueOf(despesa.getSalarioTecn()));
                jcAno.setSelectedItem(despesa.getAnoDispesa());
                jcMes.setSelectedItem(despesa.getMesDispesa());
                jcAno.setEnabled(false);
                jcMes.setEnabled(false);
                btPesqusar1.setEnabled(false);
                calcularResultado();
                btExcluir.setEnabled(true);
            }

        } else {
            JOptionPane.showMessageDialog(null, "Para efetuar a pesquisa, informe o Ano e Curso!", "Erro", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btPesquisarActionPerformed

