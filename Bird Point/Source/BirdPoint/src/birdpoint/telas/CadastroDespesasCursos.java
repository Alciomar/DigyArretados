    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        if (Util.chkVazio(tfCurso.getText(), tfDespesaTotal.getText(), tfDespesasDeEncargos.getText(), tfDespesasGerais.getText(), tfDespesasOperacionais.getText(), tfReceita.getText(), tfResultado.getText(), tfSalarioCoor.getText(), tfSalarioProf.getText(), tfSalarioTecnic.getText(), String.valueOf(jcAno.getSelectedItem()), String.valueOf(jcMes.getSelectedItem()))) {
            despesa.setAnoDispesa(Integer.parseInt(String.valueOf(jcAno.getSelectedItem())));
            despesa.setMesDispesa(String.valueOf(jcMes.getSelectedItem()));
            despesa.setCurso(curso);
            despesa.setDespesasDeEncargos(Double.parseDouble(tfDespesasDeEncargos.getText()));
            despesa.setDespesasGerais(Double.parseDouble(tfDespesasGerais.getText()));
            despesa.setDespesasOperacionais(Double.parseDouble(tfDespesasOperacionais.getText()));
            despesa.setReceitaCurso(Double.parseDouble(tfReceita.getText()));
            despesa.setResultadoDespesa(resultado);
            despesa.setSalarioCoor(Double.parseDouble(tfSalarioCoor.getText()));
            despesa.setSalarioProf(Double.parseDouble(tfSalarioProf.getText()));
            despesa.setSalarioTecn(Double.parseDouble(tfSalarioTecnic.getText()));
            despesa.setTotalDespesa(totalDespesa);
            despesasDAO.salvar(despesa);
            btLimparActionPerformed(null);
        }
    }//GEN-LAST:event_btSalvarActionPerformed

