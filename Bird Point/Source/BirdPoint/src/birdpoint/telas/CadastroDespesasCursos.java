    private void tfReceitaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfReceitaKeyTyped
        int ascii = evt.getKeyChar();
        if (!(ascii >= 48 && ascii <= 57) && !(ascii == 46) && !(ascii == evt.VK_BACK_SPACE)) {
            evt.consume();
        }
        if (tfReceita.getText().length() >= 12) {
            evt.consume();
        }
    }//GEN-LAST:event_tfReceitaKeyTyped

