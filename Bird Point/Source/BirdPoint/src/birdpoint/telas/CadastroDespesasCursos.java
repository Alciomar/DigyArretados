    public void preencherAnos() {
        dataAno = new Date();
        int anoAtual = Integer.parseInt(formatarData.format(dataAno));
        anoAtual--;
        for (int i = 0; i < 4; i++) {
            jcAno.addItem(anoAtual);
            anoAtual++;
        }
        anoAtual = Integer.parseInt(formatarData.format(dataAno));
        jcAno.setSelectedItem(anoAtual);
    }

    public void calcularResultado() {
        double somaDespesas = 0;
        try {
            somaDespesas += Double.parseDouble(tfSalarioCoor.getText());
            somaDespesas += Double.parseDouble(tfSalarioProf.getText());
            somaDespesas += Double.parseDouble(tfSalarioTecnic.getText());
            somaDespesas += Double.parseDouble(tfDespesasDeEncargos.getText());
            somaDespesas += Double.parseDouble(tfDespesasGerais.getText());
            somaDespesas += Double.parseDouble(tfDespesasOperacionais.getText());

            DecimalFormat df = new DecimalFormat("##.00");
            double resultadoTemp = (Double.parseDouble(tfReceita.getText()) - somaDespesas);
            totalDespesa = somaDespesas;
            resultado = resultadoTemp;
            tfDespesaTotal.setText(df.format(somaDespesas));
            tfResultado.setText(df.format(resultadoTemp));

            if (resultadoTemp < 0) {
                tfSituação.setText("Negativo");
                tfSituação.setForeground(new java.awt.Color(255, 51, 0));
                tfResultado.setForeground(new java.awt.Color(255, 51, 0));
            } else if (resultadoTemp > 0) {
                tfSituação.setText("Positivo");
                tfSituação.setForeground(new java.awt.Color(0, 153, 51));
                tfResultado.setForeground(new java.awt.Color(0, 153, 51));
            }
        } catch (Exception e) {
        }
    }

